package projeto;

public class Cliente {
	// Atributos
	protected String nome;
	protected int cpf;
	
	// Métodos Acessores
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getCpf() {
		return cpf;
	}
	public void setCpf(int cpf) {
		this.cpf = cpf;
	}	
}
